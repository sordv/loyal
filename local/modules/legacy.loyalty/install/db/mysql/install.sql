DROP TABLE b_legacy_loyalty_program;

CREATE TABLE b_legacy_loyalty_program (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    TYPE VARCHAR(50) NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    ACTIVE CHAR(1) DEFAULT 'N'
);

